package com.app.entity;

public class AboutUs {
	private Integer id;
	private String ourTitle;
	private String ourDetails;
	private String createTime;
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the ourTitle
	 */
	public String getOurTitle() {
		return ourTitle;
	}
	/**
	 * @param ourTitle the ourTitle to set
	 */
	public void setOurTitle(String ourTitle) {
		this.ourTitle = ourTitle;
	}
	/**
	 * @return the ourDetails
	 */
	public String getOurDetails() {
		return ourDetails;
	}
	/**
	 * @param ourDetails the ourDetails to set
	 */
	public void setOurDetails(String ourDetails) {
		this.ourDetails = ourDetails;
	}
	/**
	 * @return the createTime
	 */
	public String getCreateTime() {
		return createTime;
	}
	/**
	 * @param createTime the createTime to set
	 */
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
}
