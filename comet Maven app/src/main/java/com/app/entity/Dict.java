package com.app.entity;

public class Dict {
	private String ky;
	private String vl;
	private String tp;
	/**
	 * @return the ky
	 */
	public String getKy() {
		return ky;
	}
	/**
	 * @param ky the ky to set
	 */
	public void setKy(String ky) {
		this.ky = ky;
	}
	/**
	 * @return the vl
	 */
	public String getVl() {
		return vl;
	}
	/**
	 * @param vl the vl to set
	 */
	public void setVl(String vl) {
		this.vl = vl;
	}
	/**
	 * @return the tp
	 */
	public String getTp() {
		return tp;
	}
	/**
	 * @param tp the tp to set
	 */
	public void setTp(String tp) {
		this.tp = tp;
	}
	
}
